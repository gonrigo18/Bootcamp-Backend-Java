package clase1;

public class TiposDeVariables {

    public static void main(String[] args) {

        // Tipos primitivos

        // Enteros sin decimales:
        byte b; // -128 a 127
        short s; // -32768 a 32767
        int i; // -2147483648 a 2147483647
        long l; // -9223372036854775808 a

        // byte < short < int < long..

        // Enteros con decimales:
        float f = 0.0F;
        double d = 0.0;

        // Cadenas
        char c = 'c';
        String nombre = "Gonzalo";

        // Boolean
        boolean flag = true;
    }
}
